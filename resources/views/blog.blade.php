            <h2 class="mb-3">1.What Is a Coronavirus?</h2>
            <p>Chinese health officials first reported cases of acute respiratory illness associated with a seafood and animal market in the city of Wuhan on December 31, 2019. A week later, they confirmed that a novel coronavirus, SARS-CoV-2, was associated with this initial cluster of cases, according to the Centers for Disease Control and Prevention (CDC). (1) The first confirmed case of COVID-19, the disease caused by SARS-CoV-2, in the United States was reported on January 21, 2020; by the end of March, the number of cases had climbed to 188,200, according to the Johns Hopkins University Coronavirus Resource Center.A “novel” coronavirus (CoV) means it’s a new strain of coronavirus that hasn’t been seen or identified before.</p>
            <p>
              <img src="images/corona6.jpg" alt="" class="img-fluid">
            </p>
            <p>Coronaviruses can range from a common cold to more serious illnesses, like severe acute respiratory syndrome (SARS). Symptoms can be mild, with just a runny nose or cough, but can also become much more serious, including fever and even pneumonia. In some cases, a coronavirus can be fatal, especially in frailer people or those with preexisting medical conditions, such as heart disease or diabetes, notes the World Health Organization (WHO).</p>
            <p>Types of Coronaviruses</p>
            <p>Human coronaviruses were first identified in the mid-1960s, and there are currently seven that can infect people. They include 229E (alpha coronavirus), NL63 (alpha coronavirus), OC43 (beta coronavirus), HKU1 (beta coronavirus), MERS-CoV, SARS-CoV, and SARS-CoV-2, the latest coronavirus to be transmitted to humans.
            It is very common for people around the world to be infected with 229E, NL63, OC43, and HKU1. These viruses usually cause a mild to moderate upper respiratory tract illness — basically, a common cold, according to the CDC. 
            These coronaviruses can also cause lower respiratory tract illnesses, such as pneumonia and bronchitis. People with cardiopulmonary disease or weaker immune systems, as well as infants and elderly people, are at higher risk, notes the CDC. </p>
            <h2 class="mb-3 mt-5">2.Easy Ways to Improve Your Relationship With Your Doctor</h2>
            <p>Ask your doctor how electronic health records could help you monitor your health.If you become sick in the middle of the night, you might wake up your spouse, or even let work know you won't be coming in the next day. But some people — at least, those with particularly good relationships with their doctors — might send a quick email over to their physician to let them know what's going on. This sort of interaction may be part of the reason why patients who have good relationships with their doctors tend to have better health outcomes, as a new study found.</p>
            <p>
              <img src="images/image_4.jpg" alt="" class="img-fluid">
            </p>
            <p>Logically this makes sense, as people who get along with their doctors are more likely to go and see them, be honest with them, and listen to their advice. The research, conducted by doctors at Massachusetts General Hospital and published in PLOS One, analyzed 13 randomized control trials, in which doctors underwent training to improve how they interacted with patients. If the doctor-patient relationship improved, the patient's health also improved, for example, by increased weight loss or lower blood pressure. In fact, the researchers said the overall improvement seen across the 13 studies was equivalent to to the added boost that taking aspirin daily has on your risk of having a heart attack.</p>
            <p>That a good doctor-patient relationship can improve health outcomes has been shown before, for example in a study that showed diabetic patients are more successful at monitoring their blood sugar when their doctor is more empathetic, said Rodney Tucker, MD, chief experience officer for the University of Alabama at Birmingham Health System who was not involved in the study. Tucker said the finding is "common sense.</p>
            <h2 class="mb-3 mt-5">3. Why Your Kids Should Still Get Vaccinated During the Coronavirus Pandemic</h2>
            <p>The ongoing COVID-19 (coronavirus) pandemic has made it difficult to keep up with routine health maintenance, whether that’s non-essential surgery, going to the emergency room or regular visits to your primary health care provider. Given the outbreak, it’s understandable there are a lot of questions about where it’s safe to go and if certain offices are even open. And one area of concern pertains to vaccinations of young children.Fears over COVID-19 infections plus broad and varying “stay-at-home” orders across many states have resulted in a reported decrease in children receiving routine, essential immunizations. Pediatrician Kimberly Giuliano, MD, points out, “Since the onset of the pandemic, national data shows there’s been from a 42 to 73% decline in vaccination rates, depending on the source and vaccine type.Missing these routine vaccinations, though, can be harmful for children, leaving them exposed to numerous other illnesses that carry more risks for them than COVID-19, including measles. We spoke with pediatrician Skyler Kalady, MD, about those risks, concerns over COVID-19, and why parents should make sure their children stay updated on their vaccinations. </p>
            <p>
              <img src="images/child.jpg" alt="" class="img-fluid">
            </p>
            <p>While children are at risk for COVID-19, Dr. Kalady says, “Fortunately, studies show that most children who develop COVID-19 have a relatively mild course of illness, or are even asymptomatic, compared to adults.” She adds that parents should continue to prioritize getting their children their routine vaccinations in spite of anxieties over COVID-19.“Vaccine preventable illness are present in our community. It’s really important that we don’t lose sight of these real health threats,” she says. “If children are not vaccinated, they are at high risk for developing those vaccine preventable illnesses, including measles and pertussis (whooping cough).” </p>
