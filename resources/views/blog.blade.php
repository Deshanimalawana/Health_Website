              <h3 class="heading-sidebar">How Are Coronaviruses Treated?</h3>
              <p>There is currently no specific treatment for a coronavirus. Antibiotics do not work against viruses; they are only effective in treating bacterial infections. Treating and relieving symptoms is the appropriate care, according to the WHO. People who have been exposed to COVID-19 should contact their health provider for further instruction. If you test positive for COVID-19, your doctor will instruct you on how to monitor your symptoms as well as what to do if they worsen.People confirmed to have COVID-19 may be eligible to participate in one of the ongoing clinical trials. </p>
              <h3 class="heading-sidebar">How Can You Prevent Being Infected With a Coronavirus?</h3>
              <p>To minimize your chances of getting a coronavirus, including COVID-19, practice the same habits you would to avoid catching any infectious respiratory virus. Per the CDC, these include:
              Follow federal and local stay-at-home orders. "Stay at home" is pretty self-explanatory: Work and engage in schooling from home if possible and don’t go anywhere that isn’t essential. Avoid social gatherings with groups of more than 10 people.
              Practice social distancing. Keep at least six feet between yourself and others.
              Try not to touch your eyes, nose, and mouth with unwashed hands.
              Practice home isolation if you’re sick. Separate yourself from other people and pets in your home and use a different bathroom if possible.
              If you have to sneeze or cough, cover with a tissue and then dispose of the tissue.
              Clean and disinfect things or surfaces that you touch a lot, such as phones, doorknobs, and countertops.
              Wash your hands frequently with soap and water for at least 20 seconds. If you don’t have access to a sink, use an alcohol-based hand sanitizer with at least 60 percent alcohol.
              Cover your mouth and nose with a cloth face cover when you are around others.</p>
